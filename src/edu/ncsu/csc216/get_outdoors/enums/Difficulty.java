package edu.ncsu.csc216.get_outdoors.enums;

/**
 * Enumeration names the trail difficulty options: EASY, MODERATE, CHALLENGING,
 * DIFFICULT, VERY_DIFFICULT, EXTREME.
 * 
 * @author kavitpatel
 */
public enum Difficulty {
	
	/** Represents the easy difficulty of the trail */
	EASY,
	/** Represents the moderate difficulty of the trail */
	MODERATE,
	/** Represents the challenging difficulty of the trail */
    CHALLENGING,
    /** Represents the difficult difficulty of the trail */
	DIFFICULT,
	/** Represents the very difficult difficulty of the trail */
	VERY_DIFFICULT,
	/** Represents the extreme difficulty of the trail */
	EXTREME,

}
